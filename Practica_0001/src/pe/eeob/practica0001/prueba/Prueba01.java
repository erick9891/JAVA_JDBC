    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Connection cn = null;
        try {
            // Acceso al objeto conexi√≥n
            cn = AccesoDB.getConnection();
            // Inicia Tx
            cn.setAutoCommit(false);
            
            
            String sql = "SELECT CHR_CLIECODIGO, VCH_CLIENOMBRE, VCH_CLIEPATERNO, VCH_CLIEMATERNO, CHR_CLIEDNI "
                    + "FROM CLIENTE "
                    + "WHERE VCH_CLIENOMBRE LIKE ?";
            PreparedStatement pstm = cn.prepareStatement(sql);
            
            String s1 = txtNombre.getText();
            String s2 = s1 + "%";
            
            pstm.setString(1, s2);
            
            // Resultados
            ResultSet rs = pstm.executeQuery();
            
            String s="";
            while (rs.next()){
                String codigo = rs.getString("CHR_CLIECODIGO");
                String nombre = rs.getString("VCH_CLIENOMBRE");
                String paterno = rs.getString("VCH_CLIEPATERNO");
                String materno = rs.getString("VCH_CLIEMATERNO");
                String dni = rs.getString("CHR_CLIEDNI");
                s += "Codigo: " + codigo + ", Nombre: " + nombre + " "
                        + paterno + " " + materno + ", DNI: " + dni + "\n";
            }
            
            txtResultado.setText(s);
            
            // Confirma Tx
            cn.commit();
        } catch (Exception e) {
            try {
                cn.rollback();
            } catch (Exception e1) {
            }
            JOptionPane.showMessageDialog(rootPane, e.getMessage());
        } finally {
            try {
            cn.close();
            } catch (Exception e) {
            }
        }
        
    }//GEN-LAST:event_jButton1ActionPerformed

